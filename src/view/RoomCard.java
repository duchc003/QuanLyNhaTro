    private void btnChiTietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChiTietActionPerformed
        ChiTietPhongTro ct = new ChiTietPhongTro();
        List<PhongView> list = phongServce.getALL();
        if (list.size() >= 0) {
            List<KhachThuePhong> listLoad = phongServce.getKhachThuePhong(txtTenPhong.getText());
            ct.loadTableKhachThuePhong(listLoad);
            List<DinhVuPhong> dinhVuPhong = phongServce.getDinhVuPhong(txtTenPhong.getText());
            ct.loadTableDinhVu(dinhVuPhong);
            List<ThietBiPhong> thietBiPhong = phongServce.getThietBiPhong(txtTenPhong.getText());
            ct.loadTableThietBi(thietBiPhong);
            for (int i = 0; i < list.size(); i++) {
                ct.getMaPhong().setText(list.get(i).getMaPhong());
                ct.getKhuVuc().setText(list.get(i).getKhuVuc());
                ct.getLoaiPhong().setText(list.get(i).getLoaiPhong());
                ct.getSoNguoiToiDa().setText(String.valueOf(list.get(i).getSoLuongNguoi()));
                ct.getDienTich().setText(String.valueOf(list.get(i).getDienTich()));
                ct.getGiaPhong().setText(String.valueOf(list.get(i).getGiaPhong()));
                ct.getTrangThai().setSelectedItem(txtTrangThai.getText());
            }
        }
        ct.setDefaultCloseOperation(FromHopDong.DO_NOTHING_ON_CLOSE);
        ct.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                // Xử lý sự kiện đóng cửa sổ form b mà không tắt form a
                ct.setVisible(false);
            }
        });
        ct.setVisible(true);
    }//GEN-LAST:event_btnChiTietActionPerformed

    private void btnHopDongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHopDongActionPerformed
        if (txtTrangThai.getText().equals("Đã Đủ")) {
            JOptionPane.showMessageDialog(this, "Phòng đã đủ");
        } else if (Integer.parseInt(lblSo.getText()) == Integer.parseInt(txtSoLuong.getText())) {
            JOptionPane.showMessageDialog(this, "Phòng Đã Full Người Không Thể Thêm Người Vào Phòng");
        } else {
            FromHopDong fromHopDong = new FromHopDong();
            fromHopDong.getMaPhong().setText(txtTenPhong.getText());
            fromHopDong.getKhuVuc().setText(txtTang.getText());
            fromHopDong.getLoaiPhong().setText(txtLoaiPhong.getText());
            fromHopDong.getSoNguoiToiDa().setText(txtSoLuong.getText());
            fromHopDong.getDienTich().setText(txtDienTich.getText());
            fromHopDong.getGiaPhong().setText(txtGia.getText());
            fromHopDong.setDefaultCloseOperation(FromHopDong.DO_NOTHING_ON_CLOSE);
            fromHopDong.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosing(WindowEvent e) {
                    // Xử lý sự kiện đóng cửa sổ form b mà không tắt form a
                    fromHopDong.setVisible(false);
                }
            });
            fromHopDong.setVisible(true);
        }
    }//GEN-LAST:event_btnHopDongActionPerformed

