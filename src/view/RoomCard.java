    private void isContractCreated() {
        int so = Integer.parseInt(lblSo.getText());
        if (so >= 1) {
            btnHopDong.setVisible(false);
            btnPhuThuoc.setVisible(true);
        } else {
            btnHopDong.setVisible(true);
            btnPhuThuoc.setVisible(false);
        }
    }

    public void mauSacCard() {

        if (txtTrangThai.getText().equals("Còn Trống")) {
            pnl.setBackground(new Color(215, 233, 186));
        } else {
            pnl.setBackground(new Color(248, 135, 127)); // thiết lập màu đỏ nếu phòng đã đầy
        }
    }

    private void loadSoNguoi(List<KhachThuePhong> list) {
        for (KhachThuePhong khachThuePhong : list) {
            lblSo.setText(String.valueOf(khachThuePhong.getId()));
        }
    }

